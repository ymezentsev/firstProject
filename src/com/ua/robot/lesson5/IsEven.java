package com.ua.robot.lesson5;

public class IsEven {
    public static void main(String[] args) {
        int number = 10;
        if (number % 2 == 0) {
            System.out.println("Number = " + number + " is even");
        }
        else {
            System.out.println("Number = " + number + " is odd");
        }
    }
}
